package za.co.invoketech.store.presentation.model;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.ArrayList;
import java.util.List;

import javax.faces.bean.ManagedBean;
import javax.faces.bean.SessionScoped;

/**
 * @author a.carel.g.nel@gmail.com (Carel Nel)
 */

@SessionScoped
@ManagedBean
public class ProductDisplayBean implements Serializable {
		
	private static final long serialVersionUID = 1L;
	private String image;
	private String title;
	private long id;
	private BigDecimal price;
	private long stock;
	
	public ProductDisplayBean() {
		image = "/images/123456/img_123456_1.jpg";
		title = "test";
	}

	public String getImage() {
		return image;
	}

	public void setImages(String image) {
		this.image = image;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}

	public long getStock() {
		return stock;
	}

	public void setStock(long stock) {
		this.stock = stock;
	}
}